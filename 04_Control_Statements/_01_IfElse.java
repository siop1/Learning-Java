
public class _01_IfElse {

    public static void main(String[] args) {
        int num = 27;
        if (num > 0) {
            System.out.println(num + " is positive");
        } else if (num < 0) {
            System.out.println(num + " is negative");
        }
        else{
            System.out.println("Number is zero");
        }
    }
}
